const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');
// Fallback uses existing list on disk; glob not required

const outputFile = path.join(
  __dirname,
  '..',
  'frontend',
  'src',
  'pages',
  'docs',
  'md',
  'new-quests-v3.md'
);

function getBaseRef() {
  try {
    execSync('git show-ref --verify --quiet refs/remotes/origin/main');
    return 'origin/main';
  } catch (err) {
    return 'main';
  }
}

function getNewQuestPaths() {
  try {
    // Ensure enough history for a merge base even in shallow clones
    execSync('git fetch origin main --depth=100000', { stdio: 'ignore' });
  } catch (err) {
    // ignore fetch errors
  }
  const baseRef = getBaseRef();
  try {
    execSync(`git merge-base ${baseRef} HEAD`, { stdio: 'ignore' });
  } catch (err) {
    return readPathsFromFile();
  }
  try {
    const diff = execSync(
      `git diff --name-only --diff-filter=A ${baseRef}...HEAD -- frontend/src/pages/quests/json`,
      { encoding: 'utf8' }
    );
    return diff
      .split('\n')
      .map((p) => p.trim())
      .filter(Boolean);
  } catch (err) {
    return [];
  }
}

function readPathsFromFile() {
  try {
    const content = fs.readFileSync(outputFile, 'utf8');
    return content
      .split('\n')
      .filter((line) => line.startsWith('- '))
      .map((line) => `frontend/src/pages/quests/json/${line.slice(2)}.json`);
  } catch (err) {
    return [];
  }
}

function groupQuests(paths) {
  const groups = {};
  for (const p of paths) {
    const parts = p.split('/');
    const tree = parts[parts.length - 2];
    const quest = parts[parts.length - 1].replace('.json', '');
    if (!groups[tree]) groups[tree] = [];
    groups[tree].push(`${tree}/${quest}`);
  }
  return Object.keys(groups)
    .sort()
    .map((tree) => ({ tree, quests: groups[tree].sort() }));
}

function generateMarkdown(groups) {
  const lines = [
    '---',
    "title: 'New Quests in v3'",
    "slug: 'new-quests-v3'",
    '---',
    '',
    '<!-- Auto-generated by scripts/update-new-quests-v3.js. Run `npm run new-quests:update` to refresh. -->',
    '',
    '# Quests added in v3',
    '',
    'These quests exist in the `v3` branch but are not present on `main` yet. Use this list when upgrading quests or proposing follow-up content.',
    '',
  ];
  groups.forEach(({ tree, quests }) => {
    lines.push(`### ${tree}`);
    lines.push('');
    quests.forEach((q) => lines.push(`- ${q}`));
    lines.push('');
  });
  if (lines[lines.length - 1] === '') {
    lines.pop();
  }
  return lines.join('\n') + '\n';
}

function main() {
  const paths = getNewQuestPaths();
  const groups = groupQuests(paths);
  const content = generateMarkdown(groups);
  fs.writeFileSync(outputFile, content);
}

if (require.main === module) {
  main();
}

module.exports = {
  getNewQuestPaths,
  groupQuests,
  generateMarkdown,
  main,
  readPathsFromFile,
};
